package org.sam;

import java.util.Scanner;

public class StarLoop {

	public static void main(String[] args) {
		
	
		Scanner s= new Scanner(System.in);
		
		System.out.println("6");
		int rows = s.nextInt();
		System.out.println("6");
		for(int f=rows;f>=0;f--) {
			for(int g=0;g<=f;g++) {
				System.out.print("*");
			}
			System.out.println();
		}
		
		
		
		
		
		
		
		
		
		
		
		
		 
		
		
		
		
		
		
		
	}
	
	
}
